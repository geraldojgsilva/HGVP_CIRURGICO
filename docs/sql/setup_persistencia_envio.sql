DECLARE
  PROCEDURE grant_privilege(p_sql VARCHAR2, p_message VARCHAR2) IS
  BEGIN
    EXECUTE IMMEDIATE p_sql;
    DBMS_OUTPUT.PUT_LINE(p_message);
  EXCEPTION
    WHEN OTHERS THEN
      IF SQLCODE = -1927 THEN
        DBMS_OUTPUT.PUT_LINE(p_message || ' (ja concedido).');
      ELSE
        RAISE;
      END IF;
  END;
BEGIN
  grant_privilege(
    'GRANT SELECT, INSERT, UPDATE ON DBAMV.TB_MENSAGEM_FILA_ENVIO TO ELPISSYSTEM',
    'Permissoes da fila concedidas para ELPISSYSTEM.'
  );

  grant_privilege(
    'GRANT SELECT, INSERT, UPDATE ON DBAMV.TB_MENSAGEM_LOTE_ENVIO TO ELPISSYSTEM',
    'Permissoes dos lotes concedidas para ELPISSYSTEM.'
  );

  grant_privilege(
    'GRANT SELECT, INSERT, UPDATE ON DBAMV.TB_PREPARO_CIRURGICO TO ELPISSYSTEM',
    'Permissoes dos preparos concedidas para ELPISSYSTEM.'
  );
END;
